var largestSubmatrix = function(matrix) {
	const rows = matrix.length;
	const cols = matrix[0].length;
	
	const heights = matrix[0].map(_ => 0);
	
	let out = 0;
	
	for (let i = 0; i < rows; i++) {
		for (let j = 0; j < cols; j++) {
			if (matrix[i][j] === 0) {
				heights[j] = 0;
			} else {
				heights[j] += 1;
			}
		}
		
		const sortedArray = [...heights];
		sortedArray.sort((a, b) => a - b)

		for (let j = 0; j < cols; j++) {
			out = Math.max(out, sortedArray[j] * (cols - j));
		}
	}
	
	return out;
};

